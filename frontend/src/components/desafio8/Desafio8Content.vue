          sostenibilidad de una empresa de software depende de su capacidad para equilibrar la flexibilidad técnica con
          el fortalecimiento de los canales humanos de aprendizaje, tales como la mentoría y la socialización orgánica en
          el espacio de trabajo. Frente a las brechas de innovación del mercado, la firma reafirma el rol fundamental
          del profesional de sistemas: aportar valor mediante el diseño no de soluciones genéricas, sino de la mejor
          alternativa técnica y económica para los objetivos de negocio de sus clientes.
